/* kernel: raw partition */
int x2a_load_kernel_raw(uint32_t src_addr, uint32_t inlen,
	uint32_t dst_addr)
{
	char cmd[256] = { 0 };
	uint32_t offset = src_addr/512;
	uint32_t count = (inlen - 1)/512 + 1;
	int ret;
	int boot_mode = hb_boot_mode_get();

	if ((src_addr > KERNEL_MAX_ADDR) || (src_addr % 512 != 0))
		return ADDR_INVALID;

	/* load kernel image */
	if ((boot_mode == PIN_2ND_NOR) || (boot_mode == PIN_2ND_NAND))
		snprintf(cmd, sizeof(cmd), "sf read 0x%x 0x%x 0x%x", dst_addr,
			offset, count);
	else
		snprintf(cmd, sizeof(cmd), "mmc read 0x%x 0x%x 0x%x", dst_addr,
			offset, count);

	ret = run_command_list(cmd, -1, 0);
	if (ret < 0)
		return LOAD_FAILURE;

	return ALG_SUCCESS;
}

/* kernel: ext4 file system */
int x2a_load_kernel_file(char *partition_name,
	char *image_name, uint32_t dst_addr)
{
	char cmd[256] = { 0 };
	uint32_t part_id = 0;
	int ret;

	if (NULL == partition_name || NULL == image_name)
		return BAD_PARAMETER;

	part_id = get_partition_id(partition_name);
	if (CMD_RET_FAILURE == part_id)
		return BAD_PARAMETER;

	/* load kernel image */
	snprintf(cmd, sizeof(cmd), "ext4load mmc 0:%d 0x%x %s", part_id,
		dst_addr, image_name);

	ret = run_command_list(cmd, -1, 0);
	if (ret < 0)
		return LOAD_FAILURE;

	return ALG_SUCCESS;
}

int x2a_hash_get_digest_size(uint32_t algo)
{
	uint32_t digest_size = 0;

	switch(algo) {
	case CRYPTO_MODE_HASH_MD5:
		digest_size = DEGIST_MD5_SIZE;
		break;

	case CRYPTO_MODE_HASH_SHA1:
		digest_size = DEGIST_SHA1_SIZE;
		break;

	case CRYPTO_MODE_HASH_SHA224:
		digest_size = DEGIST_SHA224_SIZE;
		break;

	case CRYPTO_MODE_HASH_SHA256:
		digest_size = DEGIST_SHA256_SIZE;
		break;

	case CRYPTO_MODE_HASH_SHA384:
		digest_size = DEGIST_SHA384_SIZE;
		break;

	case CRYPTO_MODE_HASH_SHA512:
		digest_size = DEGIST_SHA512_SIZE;
		break;

	default:
		return BAD_PARAMETER;
	}

	return digest_size;
}

int x2a_get_sign_size(uint32_t algo)
{
	uint32_t sign_size = 0;

	switch(algo) {
	case ALG_RSA1024:
		sign_size = SIGN_RSA1024_SIZE;
		break;

	case ALG_RSA2048:
		sign_size = SIGN_RSA2048_SIZE;
		break;

	case ALG_RSA4096:
		sign_size = SIGN_RSA4096_SIZE;
		break;

	default:
		return BAD_PARAMETER;
	}

	return sign_size;
}

int x2a_aes_crypt_image(uint8_t *src_data,
	uint8_t *dst_data, uint32_t inlen, uint8_t *key,
	uint8_t *iv, bool enc)
{
	int ret = 0;

	if (NULL == src_data || NULL == dst_data || NULL == key || NULL == iv)
		return BAD_PARAMETER;

	printf("AES function rom test start (CBC with 128 key length)"
		" with 32byte image\n");

	ret = spacc_ex((uint64_t) src_data, (uint64_t) dst_data, inlen,
			 CRYPTO_MODE_AES_CBC, enc, 0, key,
			 AES_128_KEY_SZ, iv, AES_128_IV_SZ);

	if (ret < 0)
		return CRYPT_FAILURE;

	return ALG_SUCCESS;
}

int x2a_sha256_calculate_hash(const uint8_t *in,
	uint32_t inlen, uint8_t *hash)
{
	int ret = 0;

	if ((NULL == in) || (NULL == hash))
		return BAD_PARAMETER;

	ret = spacc_ex((uint64_t) in, (uint64_t) hash, inlen, 0, 1,
		 CRYPTO_MODE_HASH_SHA256, NULL, 0, NULL, 0);

	if (ret < 0)
		return CAL_DIGEST_FAILURE;

	return ALG_SUCCESS;
}

int x2a_auth_kernel_img(uint32_t algo, uint8_t *key,
	uint32_t salt_len, uint8_t *msg, uint32_t msg_len,
	uint8_t *sig, uint32_t sig_len)
{
	uint32_t digest_size = 0;
	uint32_t sign_size = 0;
	uint32_t alg_digest = algo & 0xff;
	uint32_t alg_sign = (algo >> 16) & 0xff;

	int ret;
	uint8_t dest[256];

	if (NULL == key || NULL == msg || NULL == sig)
		return BAD_PARAMETER;

	digest_size = x2a_hash_get_digest_size(alg_digest);
	if (digest_size != msg_len)
		return BAD_PARAMETER;

	sign_size = x2a_get_sign_size(alg_sign);
	if (sign_size != sig_len)
		return SIGNATURE_INVALID;

	ret = PKA_public_decrypt(RSA_PUB_KEY_SIZE, msg, key, dest);
	if (ret < 0)
		return VERIFY_FAILURE;

	if (memcmp(dest, sig, sizeof(dest)) == 0)
		ret = ALG_SUCCESS;
	else
		ret = VERIFY_FAILURE;

	return ret;
}

